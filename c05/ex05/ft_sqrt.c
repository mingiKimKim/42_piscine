int	ft_sqrt(int nb)
{	
	int	num;

	num = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (num <= nb / num)
	{	
		if (num * num == nb)
			return (num);
		num++;
	}
	return (0);
}
